
-- CRIAÇÃO DAS TABELAS

CREATE TABLE CLIENTE (
    ID_CLIENTE INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    CEP VARCHAR(9),
    LOGRADOURO VARCHAR(100),
    COMPLEMENTO VARCHAR(100),
    BAIRRO VARCHAR(100),
    CIDADE VARCHAR(100),
    UF VARCHAR(2),
    COD_IBGE VARCHAR(10)
);

CREATE TABLE PRODUTO (
    ID_PRODUTO INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DESCRICAO VARCHAR(100) NOT NULL,
    VALOR NUMERIC(15, 2)
);

CREATE TABLE PEDIDO (
    ID_PEDIDO INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    REFERENCIA VARCHAR(50),
    NUMERO_PEDIDO INTEGER,
    DATA_EMISSAO DATE,
    ID_CLIENTE INTEGER,
    TIPO_OPERACAO VARCHAR(10),
    TOTAL_PEDIDO NUMERIC(15, 2),
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE(ID_CLIENTE)
);

CREATE TABLE ITENS_PEDIDO (
    ID_ITEM INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_PEDIDO INTEGER,
    ID_PRODUTO INTEGER,
    QUANTIDADE NUMERIC(15, 2),
    VALOR_UNITARIO NUMERIC(15, 2),
    FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDO(ID_PEDIDO),
    FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO(ID_PRODUTO)
);
